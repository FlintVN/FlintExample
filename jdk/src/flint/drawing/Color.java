    final int value;

    private Color(int value) {
        this.value = value;
    }

    public Color(int r, int g, int b) {
        this(r, g, b, 255);
    }

    public Color(int r, int g, int b, int a) {
        if(a < 0 || a > 255)
            throw new IllegalArgumentException("Color alpha parameter outside of expected range");
        if(r < 0 || r > 255)
            throw new IllegalArgumentException("Color red parameter outside of expected range");
        if(g < 0 || g > 255)
            throw new IllegalArgumentException("Color green parameter outside of expected range");
        if(b < 0 || b > 255)
            throw new IllegalArgumentException("Color blue parameter outside of expected range");
        value = (a << 24) | (r << 16) | (g << 8) | (b << 0);
    }

    public int getRed() {
        return (value >> 16) & 0xFF;
    }

    public int getGreen() {
        return (value >> 8) & 0xFF;
    }

    public int getBlue() {
        return (value >> 0) & 0xFF;
    }

    public int getAlpha() {
        return (value >> 24) & 0xff;
    }

    public int getRGB() {
        return value;
    }
